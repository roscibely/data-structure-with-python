# Exemplo de funções em Python

def soma(a, b):
    """
    Função que soma dois números
    """
    return a + b

def subtracao(a, b):
    """
    Função que subtrai dois números
    """
    return a - b

def multiplicacao(a, b):
    """
    Função que multiplica dois números
    """
    return a * b

def divisao(a, b):
    """
    Função que divide dois números
    """
    return a / b

def main():
    """
    Função principal
    """
    print(soma(1, 2))
    print(subtracao(1, 2))
    print(multiplicacao(1, 2))
    print(divisao(1, 2))
    

if __name__ == '__main__':
    main()


